# translation 폴더의 모든 .dart 파일 처리
translation_dir = 'lib/translation'

for filename in os.listdir(translation_dir):
    if filename.endswith('.dart'):
        filepath = os.path.join(translation_dir, filename)
        file_key = filename.replace('.dart', '')
        
        # 해당 언어의 번역 가져오기 (없으면 'Share' 사용)
        share_text = translations.get(file_key, 'Share')
        
        try:
            with open(filepath, 'r', encoding='utf-8') as f:
                content = f.read()
            
            # 이미 'share' 키가 있는지 확인
            if "'share':" in content or '"share":' in content:
                print(f"Skipped {filename} - already has 'share' key")
                continue
            
            # 'new_game' 다음에 'share' 추가
            pattern = r"(\s+'new_game':.*?\n)"
            replacement = r"\1  'share': '" + share_text + "',\n"
            
            new_content = re.sub(pattern, replacement, content)
            
            if new_content != content:
                with open(filepath, 'w', encoding='utf-8') as f:
                    f.write(new_content)
                print(f"Updated {filename} with share translation: {share_text}")
            else:
                print(f"Could not find 'new_game' in {filename}")
        
        except Exception as e:
            print(f"Error processing {filename}: {e}")

print("\nAll translation files processed!")
